                <form class="itemDetailEditForm" @submit.prevent="saveEdit">
                    <div class="itemDetailField">
                        <label>Name</label>
                        <input v-model="editName" type="text" placeholder="Item name" autofocus>
                    </div>

                    <div class="itemDetailField">
                        <label>Description</label>
                        <textarea v-model="editDescription" placeholder="Description"></textarea>
                    </div>

                    <div class="itemDetailField itemDetailFieldBrand">
                        <label>Brand</label>
                        <input
                            v-model="editBrand"
                            type="text"
                            placeholder="e.g. Sea to Summit"
                            autocomplete="off"
                            @focus="brandDropdownOpen = true; brandActiveIndex = -1"
                            @blur="closeBrandDropdown"
                            @keydown.down.prevent="moveBrandIndex(1)"
                            @keydown.up.prevent="moveBrandIndex(-1)"
                            @keydown.enter.prevent="selectBrandSuggestion(brandActiveIndex)"
                            @keydown.escape="brandDropdownOpen = false; brandActiveIndex = -1"
                        >
                        <ul v-if="brandDropdownOpen && brandSuggestionsFiltered.length" ref="brandList" class="brandSuggestions">
                            <li
                                v-for="(brand, i) in brandSuggestionsFiltered"
                                :key="brand"
                                class="brandSuggestion"
                                :class="{ active: i === brandActiveIndex }"
                                @mousedown.prevent="editBrand = brand; brandDropdownOpen = false; brandActiveIndex = -1"
                            >{{ brand }}</li>
                        </ul>
                    </div>

                    <div class="itemDetailField">
                        <label>Type</label>
                        <div class="itemDetailSelectWrap">
                            <select v-model="editCategory">
                                <option value="">— none —</option>
                                <option v-for="cat in gearCategories" :key="cat" :value="cat">{{ cat }}</option>
                            </select>
                        </div>
                    </div>

                    <div class="itemDetailFieldRow">
                        <div class="itemDetailField">
                            <label>Weight</label>
                            <div class="itemDetailUnitRow">
                                <input v-model="editWeight" type="text" placeholder="0">
                                <div class="itemDetailSelectWrap itemDetailSelectUnit">
                                    <select v-model="editUnit">
                                        <option v-for="u in units" :key="u" :value="u">{{ u }}</option>
                                    </select>
                                </div>
                            </div>
                        </div>
                        <div class="itemDetailField">
                            <label>Price</label>
                            <input v-model="editPrice" type="text" placeholder="0.00">
                        </div>
                        <div class="itemDetailField">
                            <label>Qty</label>
                            <input v-model="editQty" type="text" placeholder="1">
                        </div>
                    </div>

                    <div class="itemDetailField">
                        <label>Purchase URL</label>
                        <div class="itemDetailUrlRow">
                            <input v-model="editUrl" type="text" placeholder="https://…" @keydown.enter.prevent="fetchGear">
                            <button type="button" class="lpButton lpSmall lpButtonSecondary itemDetailFetchBtn" :disabled="fetchLoading || !editUrl" @click="fetchGear">
                                {{ fetchLoading ? '…' : '⬇ Fetch' }}
                            </button>
                        </div>
                        <span v-if="fetchError" class="itemDetailFetchError">{{ fetchError }}</span>
                        <span v-if="fetchSuccess" class="itemDetailFetchSuccess">{{ fetchSuccess }}</span>
                    </div>

                    <div class="itemDetailField">
                        <label>Image <span class="itemDetailFieldOptional">(optional)</span></label>
                        <div
                            class="itemDetailDropZone"
                            :class="{ 'itemDetailDropZoneActive': imageDragOver, 'itemDetailDropZoneHasImage': editImageUrl || editImageUploading }"
                            @dragover.prevent="imageDragOver = true"
                            @dragleave="imageDragOver = false"
                            @drop.prevent="onImageDrop"
                            @click="$refs.imageFileInput.click()"
                        >
                            <input ref="imageFileInput" type="file" accept="image/png,image/jpeg,image/gif,image/webp" style="display:none" @change="onImageFileChange">
                            <template v-if="editImageUploading">
                                <span class="itemDetailDropZoneUploading">Uploading…</span>
                            </template>
                            <template v-else-if="editImageUrl">
                                <img :src="editImageUrl" class="itemDetailDropZonePreview">
                                <button type="button" class="itemDetailDropZoneRemove" @click.stop="editImageUrl = ''">×</button>
                            </template>
                            <template v-else>
                                <svg class="itemDetailDropZoneIcon" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="1.5">
                                    <rect x="3" y="3" width="18" height="18" rx="2"/>
                                    <circle cx="8.5" cy="8.5" r="1.5"/>
                                    <path d="m21 15-5-5L5 21"/>
                                </svg>
                                <span class="itemDetailDropZoneText">Drop an image here, or <strong>click to browse</strong></span>
                                <span class="itemDetailDropZoneHint">PNG, JPG up to 5MB</span>
                            </template>
                        </div>
                        <span v-if="imageUploadError" class="itemDetailFetchError">{{ imageUploadError }}</span>
                    </div>

                    <div class="itemDetailField">
                        <label>Tags</label>
                        <div class="itemDetailTagsEdit">
                            <span v-for="tag in editTags" :key="tag" class="itemDetailTagChip">
                                {{ tag }}
                                <button type="button" class="lpIconButton itemDetailTagRemove" @click="removeTag(tag)">×</button>
                            </span>
                        </div>
                        <div class="itemDetailTagInput">
                            <input v-model="tagInput" type="text" placeholder="Add a tag…" @keydown.enter.prevent="addTag">
                            <button type="button" class="lpButton lpSmall lpButtonSecondary" @click="addTag">Add</button>
                        </div>
                    </div>
                </form>

                <div class="itemDetailEditFooter">
                    <a class="lpHref close" @click="cancelEdit">Cancel</a>
                    <button class="lpButton" @click="saveEdit">Save</button>
                </div>
            </template>
        </div>
    </modal>
</template>

<script>
import modal from './modal.vue';
import { openSpeedbump } from '../services/speedbump';
import { registerDialogOpener, unregisterDialogOpener, openDialog } from '../services/dialogs';

const weightUtils = require('../utils/weight.js');

const GEAR_CATEGORIES = [
    'Pack & Bags', 'Shelter', 'Sleep', 'Clothing', 'Water', 'Food', 'Cook',
    'Navigation', 'Safety', 'Hygiene', 'Electronics', 'Essentials', 'Other',
];

const UNITS = ['oz', 'lb', 'g', 'kg'];

export default {
    name: 'ItemDetail',
    components: { modal },
    data() {
        return {
            shown: false,
            editing: false,
            item: {},
            categoryItem: null,
            category: null,
            // edit fields
            editName: '',
            editDescription: '',
            editBrand: '',
            editCategory: '',
            editWeight: '',
            editUnit: 'g',
            editPrice: '',
            editQty: '',
            editUrl: '',
            editImageUrl: '',
            editImageUploading: false,
            imageUploadError: '',
            imageDragOver: false,
            editTags: [],
            tagInput: '',
            fetchLoading: false,
            fetchError: '',
            fetchSuccess: '',
            addToListOpen: false,
            newCategoryNames: {},
            brandDropdownOpen: false,
            brandActiveIndex: -1,
        };
    },
    computed: {
        gearCategories() { return GEAR_CATEGORIES; },
        units() { return UNITS; },
        knownBrands() {
            const library = this.$store.state.library;
            if (!library || !library.items) return [];
            return [...new Set(
                library.items.map((i) => i.brand).filter(Boolean),
            )].sort();
        },
        brandSuggestionsFiltered() {
            if (!this.editBrand) return this.knownBrands;
            const q = this.editBrand.toLowerCase();
            return this.knownBrands.filter(b => b.toLowerCase().includes(q));
        },
        thumbnailImage() {
            if (this.item.image) return `https://i.imgur.com/${this.item.image}s.jpg`;
            if (this.item.imageUrl) return this.item.imageUrl;
            return '';
        },
        displayWeight() {
            if (!this.item.weight) return '0';
            return weightUtils.MgToWeight(this.item.weight, this.item.authorUnit);
        },
        allListsWithCategories() {
            const library = this.$store.state.library;
            if (!library) return [];
            return library.lists.map(list => ({
                list,
                categories: list.categoryIds.map(id => library.getCategoryById(id)).filter(Boolean),
            }));
        },
        itemUsedInLists() {
            const library = this.$store.state.library;
            if (!library || !this.item) return [];
            return library.lists.filter(list =>
                list.categoryIds.some(catId => {
                    const cat = library.getCategoryById(catId);
                    return cat && cat.categoryItems.some(ci => ci.itemId === this.item.id);
                })
            );
        },
    },
    mounted() {
        registerDialogOpener('itemDetail', ({ item, categoryItem, category, startEditing }) => {
            this.item = { ...item };
            const liveCategory = category ? this.$store.state.library.getCategoryById(category.id) : null;
            const liveCategoryItem = liveCategory && item ? liveCategory.getCategoryItemById(item.id) : null;
            this.categoryItem = liveCategoryItem ? { ...liveCategoryItem } : (categoryItem ? { ...categoryItem } : null);
            this.category = liveCategory || category || null;
            this.shown = true;
            if (startEditing) {
                this.startEdit();
            } else {
                this.editing = false;
            }
        });
    },
    beforeUnmount() {
        unregisterDialogOpener('itemDetail');
    },
    methods: {
        closeBrandDropdown() {
            setTimeout(() => { this.brandDropdownOpen = false; this.brandActiveIndex = -1; }, 150);
        },
        moveBrandIndex(dir) {
            const max = this.brandSuggestionsFiltered.length - 1;
            this.brandActiveIndex = Math.min(Math.max(this.brandActiveIndex + dir, -1), max);
            this.$nextTick(() => {
                const list = this.$refs.brandList;
                if (!list) return;
                const active = list.children[this.brandActiveIndex];
                if (active) active.scrollIntoView({ block: 'nearest' });
            });
        },
        selectBrandSuggestion(index) {
            if (index >= 0 && index < this.brandSuggestionsFiltered.length) {
                this.editBrand = this.brandSuggestionsFiltered[index];
            }
            this.brandDropdownOpen = false;
            this.brandActiveIndex = -1;
        },
        toggleStar() {
            const starred = !this.item.starred;
            this.$store.commit('updateItem', { ...this.item, starred });
            this.item = { ...this.item, starred };
        },
        setCategoryStar(n) {
            if (!this.categoryItem || !this.category) return;
            const star = this.categoryItem.star === n ? 0 : n;
            const updatedCategoryItem = { ...this.categoryItem, star };
            this.$store.commit('updateCategoryItem', { category: this.category, categoryItem: updatedCategoryItem });
            this.categoryItem = { ...updatedCategoryItem };
        },
        close() {
            this.shown = false;
            this.editing = false;
            this.addToListOpen = false;
            this.newCategoryNames = {};
        },
        navigateToList(list) {
            this.$store.commit('setDefaultList', list);
            this.$store.commit('setGearRoomOpen', false);
            this.close();
        },
        viewImage() {
            const full = this.item.image
                ? `https://i.imgur.com/${this.item.image}l.jpg`
                : this.item.imageUrl;
            if (full) openDialog('itemViewImage', full);
        },
        startEdit() {
            this.editName = this.item.name || '';
            this.editDescription = this.item.description || '';
            this.editBrand = this.item.brand || '';
            this.editCategory = this.item.category || '';
            this.editWeight = weightUtils.MgToWeight(this.item.weight, this.item.authorUnit);
            this.editUnit = this.item.authorUnit || 'g';
            this.editPrice = this.item.price != null ? this.item.price.toFixed(2) : '0.00';
            this.editQty = this.categoryItem ? this.categoryItem.qty : 1;
            this.editUrl = this.item.url || '';
            this.editImageUrl = this.item.imageUrl || '';
            this.editImageUploading = false;
            this.imageUploadError = '';
            this.imageDragOver = false;
            this.editTags = [...(this.item.tags || [])];
            this.tagInput = '';
            this.fetchError = '';
            this.fetchSuccess = '';
            this.editing = true;
        },
        cancelEdit() {
            this.editing = false;
        },
        saveEdit() {
            const weightFloat = parseFloat(this.editWeight) || 0;
            const updatedItem = {
                ...this.item,
                name: this.editName.trim(),
                description: this.editDescription.trim(),
                brand: this.editBrand.trim(),
                category: this.editCategory,
                url: this.editUrl.trim(),
                imageUrl: this.editImageUrl.trim() || undefined,
                tags: [...this.editTags],
                authorUnit: this.editUnit,
                weight: weightUtils.WeightToMg(weightFloat, this.editUnit),
                price: Math.round((parseFloat(this.editPrice) || 0) * 100) / 100,
            };
            this.$store.commit('updateItem', updatedItem);

            if (this.categoryItem && this.category) {
                const updatedCategoryItem = {
                    ...this.categoryItem,
                    qty: parseFloat(this.editQty) || 1,
                };
                this.$store.commit('updateCategoryItem', {
                    category: this.category,
                    categoryItem: updatedCategoryItem,
                });
                this.categoryItem = { ...updatedCategoryItem };
            }

            this.item = { ...updatedItem };
            this.editing = false;
        },
        removeFromList() {
            this.$store.commit('removeItemFromCategory', {
                itemId: this.item.id,
                category: this.category,
            });
            this.close();
        },
        deleteGear() {
            const callback = () => {
                this.$store.commit('removeItem', this.item);
                this.close();
            };
            const speedbumpOptions = {
                body: `Delete "${this.item.name || 'this item'}" from your item library? It will be removed from all lists.`,
            };
            openSpeedbump(callback, speedbumpOptions);
        },
        async fetchGear() {
            if (!this.editUrl || this.fetchLoading) return;
            this.fetchLoading = true;
            this.fetchError = '';
            this.fetchSuccess = '';
            try {
                const res = await fetch('/scrapeGear', {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/json' },
                    body: JSON.stringify({ url: this.editUrl }),
                });
                const data = await res.json();
                if (!res.ok) throw new Error(data.error || 'fetch failed');

                const filled = [];
                if (data.name && !this.editName) { this.editName = data.name; filled.push('name'); }
                if (data.brand && !this.editBrand) { this.editBrand = data.brand; filled.push('brand'); }
                if (data.price != null && !parseFloat(this.editPrice)) { this.editPrice = data.price.toFixed(2); filled.push('price'); }
                if (data.weight != null && !parseFloat(this.editWeight)) {
                    this.editWeight = data.weight;
                    if (data.weightUnit) this.editUnit = data.weightUnit;
                    filled.push('weight');
                }
                if (data.imageUrl && !this.item.image && !this.editImageUrl) {
                    this.editImageUrl = data.imageUrl;
                    filled.push('image');
                }
                this.fetchSuccess = filled.length
                    ? `Filled: ${filled.join(', ')}`
                    : 'No data found — fields unchanged';
            } catch (err) {
                this.fetchError = err.message;
            } finally {
                this.fetchLoading = false;
            }
        },
        addTag() {
            const tag = this.tagInput.trim().toLowerCase();
            if (tag && !this.editTags.includes(tag)) this.editTags.push(tag);
            this.tagInput = '';
        },
        removeTag(tag) {
            this.editTags = this.editTags.filter((t) => t !== tag);
        },
        onImageDrop(evt) {
            this.imageDragOver = false;
            const file = evt.dataTransfer.files[0];
            if (file) this.uploadImageFile(file);
        },
        onImageFileChange(evt) {
            const file = evt.target.files[0];
            if (file) this.uploadImageFile(file);
            evt.target.value = '';
        },
        async uploadImageFile(file) {
            const VALID = ['image/png', 'image/jpeg', 'image/jpg', 'image/gif', 'image/webp'];
            if (!VALID.includes(file.type)) {
                this.imageUploadError = 'File must be PNG, JPG, GIF or WebP.';
                return;
            }
            if (file.size > 5 * 1024 * 1024) {
                this.imageUploadError = 'File must be under 5 MB.';
                return;
            }
            this.imageUploadError = '';
            this.editImageUploading = true;
            try {
                const formData = new FormData();
                formData.append('image', file);
                const res = await fetch('/imageUpload', { method: 'POST', body: formData, credentials: 'same-origin' });
                const data = await res.json();
                if (!res.ok) throw new Error(data.message || 'Upload failed');
                this.editImageUrl = data.data.url;
                this.$store.commit('updateItemImageUrl', { imageUrl: data.data.url, item: this.item });
            } catch (err) {
                this.imageUploadError = err.message;
            } finally {
                this.editImageUploading = false;
            }
        },
        addToCategory(category) {
            this.$store.commit('addItemToCategory', {
                itemId: this.item.id,
                categoryId: category.id,
                dropIndex: category.categoryItems.length,
            });
            this.close();
        },
        createCategoryAndAdd(listId) {
            const name = (this.newCategoryNames[listId] || '').trim();
            if (!name) return;
            this.$store.commit('createCategoryAndAddItem', {
                itemId: this.item.id,
                name,
                listId,
            });
            this.close();
        },
    },
};
</script>
